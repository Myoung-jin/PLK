package com.example.hi;

public class Day
{
	private String day;
	private boolean inMonth;

	public String getDay()
	{
		return day;
	}

	public void setDay(String day)
	{
		this.day = day;
	}

	public boolean isInMonth()
	{
		return inMonth;
	}

	public void setInMonth(boolean inMonth)
	{
		this.inMonth = inMonth;
	}

}
